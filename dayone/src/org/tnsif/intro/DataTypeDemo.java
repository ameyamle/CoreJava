package org.tnsif.intro;

public class DataTypeDemo {

	public static void main(String[] args) {
		//byte type
		byte a = 127;
		
		//integer type
		int b = 128;
		long b1 = 423154654334l;
		
		//float type
		float c = 12/5;
		float d = 5/2.5f;
		//double type
		double e = 12/5;
		double f = 12d/5d;
		
		//character type
		char z = 'a';
		
		int y = 'A';
		//String type
		String x = "ABC";

		//Boolean type
		boolean w = true;
		
		System.out.println("The value of a is:"+a);
		System.out.println("The value of b is:"+b);
		System.out.println("The value of c is:"+c);
		System.out.println("The value of d is:"+d);
		System.out.println("The value of e is:"+e);
		System.out.println("The value of f is:"+f);
		System.out.println("The value of long data type is:"+b1);
		System.out.println("The value of char data type is:"+z);
		System.out.println("The value of char data type is:"+y);
		System.out.println("The value of string data type is:"+x);
		System.out.println("The value of boolean data type is:"+w);



	}

}
